from trl import DPOTrainer, DPOConfig
from datasets import Dataset
import json

# ---- Load dataset ----
with open("dpo_dataset.json") as f:
    raw_data = json.load(f)

# ---- Format dataset ----
dpo_dataset = Dataset.from_list([
    {
        "prompt": d["prompt"],
        "chosen": d["chosen"],
        "rejected": d["rejected"]
    }
    for d in raw_data
])

print(f"DPO Dataset: {len(dpo_dataset)} preference pairs")

# ---- DPO Config ----
dpo_config = DPOConfig(
    output_dir="./outputs/dpo_model",
    num_train_epochs=3,
    per_device_train_batch_size=1,
    gradient_accumulation_steps=8,
    learning_rate=5e-5,
    beta=0.1,
    logging_steps=10,
    report_to="wandb",
    run_name="creative-refusal-dpo",
)

# ---- DPO Trainer (FIXED) ----
dpo_trainer = DPOTrainer(
    model=model,
    ref_model=None,
    args=dpo_config,
    train_dataset=dpo_dataset,
)

# ---- Train ----
print("Starting DPO Training...")
dpo_trainer.train()

# ---- Save ----
dpo_trainer.save_model("./outputs/dpo_model")

print("DPO Training complete!")

